void add(int m, int n){
	printf("Sum of two integers : %d\n", m+n);
}
void sub(int n1, int n2){
	printf("Difference of two integers : %d\n", n1-n2);
}
void prod(int n1, int n2){
	printf("Product of two integers : %d\n", n1 * n2);
}
void div(int n1, int n2){
	printf("Division of two integers: %d\n", n1/n2);
}
void rem(int n1, int n2){
	printf("Remainder of two integers: %d\n", n1%n2);
}
